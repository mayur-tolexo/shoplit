// Regenerates web/app/fonts/data.ts — the brand share-card fonts (static TTF
// instances) embedded as base64 strings.
//
// WHY EMBED: `next/og` (satori) needs static TTF/OTF buffers, and our routes run
// under the self-hosted `output: "standalone"` build. A relative `fs.readFile`
// of a sibling .ttf is NOT picked up by Next's file tracer, so the .ttf never
// lands in `.next/standalone` and the font load fails at runtime. Embedding the
// bytes in a JS module makes them part of the traced bundle — always available.
//
// Run after replacing/adding a .ttf:  pnpm gen-share-fonts

import { readFileSync, writeFileSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const fontsDir = join(here, "..", "app", "fonts");

const FACES = {
  interRegular: "Inter-Regular.ttf",
  interSemiBold: "Inter-SemiBold.ttf",
  frauncesRegular: "Fraunces-Regular.ttf",
  frauncesSemiBold: "Fraunces-SemiBold.ttf",
};

let out =
  "// AUTO-GENERATED by scripts/gen-share-fonts.mjs — do not edit by hand.\n" +
  "// Brand share-card fonts (static TTF instances) embedded as base64 so the\n" +
  "// `next/og` (satori) renderer can decode them at request time WITHOUT a\n" +
  "// filesystem read. Embedding sidesteps the standalone file-tracing problem:\n" +
  "// a relative `readFile` of a sibling .ttf is NOT traced into .next/standalone,\n" +
  "// so the bytes must live in the JS bundle. Regenerate with: pnpm gen-share-fonts.\n" +
  "// Faces: Inter 400/600 (sans), Fraunces 400/600 (serif).\n\n" +
  "/* eslint-disable */\n";

for (const [key, file] of Object.entries(FACES)) {
  const b64 = readFileSync(join(fontsDir, file)).toString("base64");
  out += `export const ${key} = "${b64}";\n`;
}

const target = join(fontsDir, "data.ts");
writeFileSync(target, out);
console.log(`wrote ${target} (${(statSync(target).size / 1024).toFixed(0)} KB)`);
